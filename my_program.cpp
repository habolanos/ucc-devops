#include <stdio.h>
#include <math.h>
#include <iostream>

using namespace std;

int main() {

	int ivalor = 0;
	int icontador= 0;

	cin >> "Ingrese un valor para calcular una suma";

	for (i = 1; i <= ivalor; i++) {
		icontador+=i;
	}

	cout << "El resultado es: " << icontador; 

	getch();

	return 0;
}
